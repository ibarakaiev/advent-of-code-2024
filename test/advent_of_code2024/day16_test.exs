defmodule AdventOfCode2024.Day16Test do
  use ExUnit.Case

  @input1 """
  ###############
  #.......#....E#
  #.#.###.#.###.#
  #.....#.#...#.#
  #.###.#####.#.#
  #.#.#.......#.#
  #.#.#####.###.#
  #...........#.#
  ###.#.#####.#.#
  #...#.....#.#.#
  #.#.#.###.#.#.#
  #.....#...#.#.#
  #.###.#.#.#.#.#
  #S..#.....#...#
  ###############
  """

  @input2 """
  #################
  #...#...#...#..E#
  #.#.#.#.#.#.#.#.#
  #.#.#.#...#...#.#
  #.#.#.#.###.#.#.#
  #...#.#.#.....#.#
  #.#.#.#.#.#####.#
  #.#...#.#.#.....#
  #.#.#####.#.###.#
  #.#.#.......#...#
  #.#.###.#####.###
  #.#.#...#.....#.#
  #.#.#.#####.###.#
  #.#.#.........#.#
  #.#.#.#########.#
  #S#.............#
  #################
  """

  test "part 1.1" do
    assert AdventOfCode2024.Day16.solve(@input1, part: 1) == 7036
  end

  test "part 1.2" do
    assert AdventOfCode2024.Day16.solve(@input2, part: 1) == 11_048
  end

  test "part 2.1" do
    assert AdventOfCode2024.Day16.solve(@input1, part: 2) == 45
  end

  test "part 2.2" do
    assert AdventOfCode2024.Day16.solve(@input2, part: 2) == 64
  end
end
